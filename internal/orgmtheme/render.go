func cssColor(hex string) string {
	h := strings.TrimPrefix(hex, "#")
	switch len(h) {
	case 6:
		return "#" + h
	case 8:
		red, green, blue, ok := hexToRGB(h)
		if !ok {
			return "#" + h
		}
		alpha, err := strconv.ParseInt(h[6:8], 16, 64)
		if err != nil {
			return "#" + h
		}
		alphaText := strings.TrimRight(strings.TrimRight(fmt.Sprintf("%.3f", float64(alpha)/255), "0"), ".")
		return fmt.Sprintf("rgba(%d, %d, %d, %s)", red, green, blue, alphaText)
	default:
		return "#" + h
	}
}

func hexToRGB(hex string) (int, int, int, bool) {
	h := strings.TrimPrefix(hex, "#")
	if len(h) < 6 {
		return 0, 0, 0, false
	}
	red, err := strconv.ParseInt(h[0:2], 16, 64)
	if err != nil {
		return 0, 0, 0, false
	}
	green, err := strconv.ParseInt(h[2:4], 16, 64)
	if err != nil {
		return 0, 0, 0, false
	}
	blue, err := strconv.ParseInt(h[4:6], 16, 64)
	if err != nil {
		return 0, 0, 0, false
	}
	return int(red), int(green), int(blue), true
}
